class ClassDetail {
  final String classCode;
  final String subjectCode;
  final String academicYear;
  final String room;
  final String type;
  final int studentCount;

  ClassDetail({
    required this.classCode,
    required this.subjectCode,
    required this.academicYear,
    required this.room,
    required this.type,
    required this.studentCount,
  });
}